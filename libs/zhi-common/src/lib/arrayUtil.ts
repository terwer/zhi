/**
 * 数组工具类
 *
 * @author terwer
 */
class ArrayUtil {
  /**
   * 判断数组是否为空
   *
   * @param arr - 数组
   */
  public static isEmptyArray<T>(arr: T[] | null | undefined): boolean {
    return !arr || !Array.isArray(arr) || arr.length === 0
  }
}
